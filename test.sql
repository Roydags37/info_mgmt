CREATE TABLE employees(
id INT AUTO_INCREMENT,
   first_name VARCHAR(100),
   last_name VARCHAR(100),
   age INT,
   position VARCHAR(100),
   address VARCHAR(255),
   PRIMARY KEY(id)
);


